    private void btnGenerateBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateBillActionPerformed
        String customerName = txtCusName.getText();
        String customerMobileNumber = txtCusMobileNumber.getText();
        String customerEmail = txtCusEmail.getText();
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
        Date date = new Date();
        String  todaydate = dFormat.format(date);
        String total = String.valueOf(grandTotal);
        String createdBy = userEmail;
        Bill bill = new Bill();
        bill.setId(billId);
        bill.setName(customerName);
        bill.setMobileNumber(customerMobileNumber);
        bill.setEmail(customerEmail);
        bill.setDate(todaydate);
        bill.setTotal(total);
        bill.setCreatedBy(createdBy);
        BillDao.save(bill);
        
        
            // Código para crear el PDF
        String userHome = System.getProperty("user.home");
        String filePath = userHome + File.separator + "Documents" + File.separator + billId + ".pdf";
        
        try (Document document = new Document(new PdfDocument(new PdfWriter(filePath)))) {
            // Añade el contenido al documento
            document.add(new Paragraph("                                    OneDestiny Sistema \n"));
            document.add(new Paragraph("---------------------------------------------------------"));
            document.add(new Paragraph("\tBill ID:" + billId + "\nCustomer Name: " + customerName + " Total Pagado: " + grandTotal));
            
            // Crea la tabla con los datos de jTable2
            Table table = new Table(4);
            table.addCell(new Cell().add(new Paragraph("Nombre")));
            table.addCell(new Cell().add(new Paragraph("Precio")));
            table.addCell(new Cell().add(new Paragraph("Cantidad")));
            table.addCell(new Cell().add(new Paragraph("Total")));
            
            for (int i = 0; i < jTable2.getRowCount(); i++) {
                String n = jTable2.getValueAt(i, 0).toString();
                String d = jTable2.getValueAt(i, 1).toString();
                String r = jTable2.getValueAt(i, 2).toString();
                String q = jTable2.getValueAt(i, 3).toString();
                table.addCell(n);
                table.addCell(d);
                table.addCell(r);
                table.addCell(q);
            }

            document.add(table);
            document.add(new Paragraph("Gracias, por tu visita, te esperamos pronto"));
            
            // Abre el PDF recién creado
            OpenPDF.openById(String.valueOf(billId));
        } catch (IOException ex) {
            ex.printStackTrace();
        }

        // Cierra la ventana actual y abre una nueva instancia de PlaceOrder
        setVisible(false);
        new PlaceOrder(createdBy).setVisible(true);
    }//GEN-LAST:event_btnGenerateBillActionPerformed

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        int index = jTable2.getSelectedRow();
        int a = JOptionPane.showConfirmDialog(null, "Estas seguro que quieres quitar ese producto", "Selecciona", JOptionPane.YES_NO_OPTION);
        if(a == 0) {
            TableModel model = jTable2.getModel();
            String total = model.getValueAt(index, 3).toString();
            grandTotal = grandTotal - Integer.parseInt(total);
            lblGrandTotal.setText(String.valueOf(grandTotal));
            ((DefaultTableModel) jTable2.getModel()).removeRow(index);
        }
    }//GEN-LAST:event_jTable2MouseClicked

